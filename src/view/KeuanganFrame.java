    //memuat ulang data kedalam jtabel
    private void loadTable() {
        
        //model tabel dengan nama kolom
        DefaultTableModel model = new DefaultTableModel(new Object[]{"Tanggal", "Keterangan", "Nominal", "Kategori"}, 0);
        
        //mengambil semua transaksi dari service 
        for (Transaksi t : service.getAll()){
            model.addRow(new Object[]{
                t.getTanggal(),
                t.getKeterangan(),
                t.getNominal(),
                t.getKategori()
            });
        }
        //menampilkan model ke tabel gui
        tblTable.setModel(model);
        lblSaldo.setText("Rp " + service.getTotalSaldo());// menampilkan total saldo ke label
    }
    
