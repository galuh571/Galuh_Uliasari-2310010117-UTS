    //fungsi untuk menambahkan transaksi ke tabel
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
      
           String tanggal = LocalDate.now().toString();//mengambil tanggal otomatis hari ini
           String ket = txtKeterangan.getText(); //mengambil teks dari inputan user
           double nominal = Double.parseDouble(txtNominal.getText()); //mengubah  input nominal dari teks ke angka
           String kategori = cmbKategori.getSelectedItem().toString(); // mengambil kategori dari combo box
           
          Transaksi transaksi;
          
          // INHERITANCE + POLYMORPHISM
          
          if (kategori.equals("Pemasukan")){
              transaksi = new TransaksiPemasukan(tanggal, ket, nominal);
          }else{
              transaksi = new TransaksiPengeluaran(tanggal, ket, nominal);
          }
              //  memakai service
           service.tambah(transaksi);
           
           // refresh tampilan tabel & saldo
           tampilData();
           tampilSaldo();
           
           JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
       
    }//GEN-LAST:event_btnTambahActionPerformed

    //fungsi untuk mengubah data transaksi
    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed

        int row = tblTable.getSelectedRow();// cek apakah user memilih baris di tabel
       if (row == -1){
           JOptionPane.showMessageDialog(this, "Pilih data yang ingin diubah");
           return;
       }
       
       
           String tanggal = LocalDate.now().toString();
           String ket = txtKeterangan.getText();
           double nominal = Double.parseDouble(txtNominal.getText());
           String kategori = cmbKategori.getSelectedItem().toString();
           
           Transaksi transaksi;
           
           // INHERITANCE  pilih objek sesuai kategori
           if (kategori.equals("Pemasukan")){
               transaksi = new TransaksiPemasukan(tanggal, ket, nominal);
           }else{
               transaksi = new TransaksiPengeluaran(tanggal, ket, nominal);
           }
           service.ubah(row, transaksi);
           tampilData();
            tampilSaldo();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
       
    }//GEN-LAST:event_btnUbahActionPerformed

    //fungsi untuk menghapus data transaksi
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblTable.getSelectedRow();
        
        
        //jika belum memilih baris
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus");
            return;
        }
        
        //mengapus transaksi di index yang di pilih
        service.hapus(row);
        tampilData();
        tampilSaldo();

        JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

    }//GEN-LAST:event_btnHapusActionPerformed
private void tampilData(){
    DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
    model.setRowCount(0);
    
    for (Transaksi t : service.getAll()){
        model.addRow(new Object[]{
            t.getTanggal(),
             t.getKeterangan(),
             t.getNominal(),
            t.getKategori()
        });
    }
}
private void tampilSaldo(){
    lblSaldo.setText("Rp. " + service.getTotalSaldo());
    
}
    //fungsi untuk export semua transaksi ke file txt
    private void btnExportTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportTxtActionPerformed
       try{
           
           //membuat file txt baru
           FileWriter fw = new FileWriter("data_keuangan.txt");
           
           //menulis setiap transaksi ke dalam file
           for (Transaksi t : service.getAll()){
               fw.write(
                        t.getTanggal() + " | " +
                                t.getKeterangan() + " | " +
                                t.getNominal() + " | " +
                                t.getKategori() + "\n" );
           }
           //menutup file
           fw.close();
           JOptionPane.showMessageDialog(this, "Export TXT berhasil!");
       }catch (Exception e){
           JOptionPane.showMessageDialog(this, "Gagal export TXT");
       }
    }//GEN-LAST:event_btnExportTxtActionPerformed

    //fungsi untuk export data ke json
    private void btnExportJsonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportJsonActionPerformed
       try{
           //membuat file json baru
           FileWriter fw = new FileWriter("data_keuangan.json");
           fw.write("[\n");
           
           int i = 0;
           
           //loop setiap transaksi
           for (Transaksi t : service.getAll()){
               
               //menulis objek json
               fw.write("  {\n");
               fw.write("    \"tanggal\": \"" + t.getTanggal() + "\",\n");
               fw.write("    \"keterangan\": \"" + t.getKeterangan() + "\",\n");
               fw.write("    \"nominal\": \"" + t.getNominal() + "\",\n");
               fw.write("    \"kategori\": \"" + t.getKategori() + "\"\n");
               fw.write("  }");
               
               //tambahkan koma kecuali di item terakhir
               if (i < service.getAll().size() - 1) fw.write(",");
               fw.write("\n");
               i++;
           }
           
           fw.write("]");
           fw.close();
           
           JOptionPane.showMessageDialog(this, "Export JSON berhasil!");
       }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Gagal export JSON");
       }

    }//GEN-LAST:event_btnExportJsonActionPerformed

